def main():
    chapter_file = '/Users/anton/proj/gita/data/chapters/chapter-13.json'

    # Load the chapter file
    with open(chapter_file, 'r', encoding='utf-8') as f:
        chapter = json.load(f)

    # Add title translations
    for lang, text in TITLE_TRANSLATIONS.items():
        chapter['title'][lang] = {"text": text, "approved": False}

    # Create a mapping of sloka number to sloka object
    sloka_map = {sloka['number']: sloka for sloka in chapter['slokas']}

    # Add translations for each sloka
    translated_count = 0
    for sloka_number, translations in SLOKA_TRANSLATIONS.items():
        if sloka_number in sloka_map:
            sloka = sloka_map[sloka_number]
            for lang, text in translations.items():
                sloka['translations'][lang] = {"text": text, "approved": False}
            translated_count += 1
        else:
            print(f"Warning: Sloka {sloka_number} not found in chapter file")

    # Update meta
    chapter['meta']['lastUpdated'] = datetime.now().strftime("%Y-%m-%dT%H:%M:%SZ")
    chapter['meta']['version'] = "1.1"

    # Save the updated chapter
    with open(chapter_file, 'w', encoding='utf-8') as f:
        json.dump(chapter, f, ensure_ascii=False, indent=2)

    print(f"Successfully added Asian translations to chapter 13")
    print(f"Title translations: 4 languages")
    print(f"Sloka translations: {translated_count} slokas x 4 languages = {translated_count * 4} translations")
    print(f"Total translated slokas: {translated_count}")

if __name__ == '__main__':
    main()
