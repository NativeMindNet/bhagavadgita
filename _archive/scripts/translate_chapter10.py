def main():
    # Read the chapter file
    with open('/Users/anton/proj/gita/data/chapters/chapter-10.json', 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Add translations to title
    title_translations = translate_title(
        data['title']['ru']['text'],
        data['title']['en']['text']
    )
    data['title'].update(title_translations)

    # Track translated slokas count
    translated_count = 0

    # Add translations to each sloka
    for sloka in data['slokas']:
        sloka_num = sloka['number']
        ru_text = sloka['translations']['ru']['text']
        en_text = sloka['translations']['en']['text']

        translations = translate_sloka(sloka_num, ru_text, en_text)
        if translations:
            sloka['translations'].update(translations)
            translated_count += 1
            print(f"Translated sloka {sloka_num}")
        else:
            print(f"WARNING: No translation found for sloka {sloka_num}")

    # Update lastUpdated timestamp
    from datetime import datetime
    data['meta']['lastUpdated'] = datetime.utcnow().strftime('%Y-%m-%dT%H:%M:%SZ')
    data['meta']['version'] = '1.1'

    # Write updated file
    with open('/Users/anton/proj/gita/data/chapters/chapter-10.json', 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"\nTotal slokas translated: {translated_count}")
    print("File saved successfully!")

if __name__ == "__main__":
    main()
